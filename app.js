
const kelvin = 0;         // assign the value 293  to kelvin//
let celcius =kelvin-273; // celcius is gotten by subtracting 273 from kelvin    
const x = 9/5;

let fahrenheit = celcius*x // fahrenheit equals celcius+33.8
fahrenheit= fahrenheit+ 32;
Math.floor(fahrenheit); /*that prints out the whole number alone*/
console.log(`The temperature is ${fahrenheit} degrees`)